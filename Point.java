import java.awt.Graphics;
import java.awt.Color;

public class Point {

	private int x;
	private int y;
	private int newY;

	public Point(int x, int y) {
		this.x = x;
		this.y = y;

	}	
	public int getX() {
		return this.x;
	}
	public int getY() {
		return this.y;
	}

	public void setY(int newY) {
		y = this.newY;
	}

}